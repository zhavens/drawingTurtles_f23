# import turtle module
import turtle

roku = turtle.Turtle()      # create turtle name Roku


# Pre-conditions: Turtle name roku exists, east orientation, spawns in top
# left of bounding box

# create definition function for letter 'I'
def I():
    roku.forward(25)
    roku.backward(12.5)
    roku.right(90)
    roku.forward(50)
    roku.right(90)
    roku.forward(12.5)
    roku.backward(25)
    
# call the function and draws 'I'
I()
    
